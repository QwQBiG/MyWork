// 购物车结算头文件

#ifndef CART_H  // 如果 CART_H 没有被定义过
#define CART_H  // 就定义它

#include "Food.h"
#include <vector>
#include <utility>

// 类名：Cart（购物车类）
// 功能：管理顾客选购的食品及数量，计算总价、打印小票
// 说明：使用 pair 将食品信息与购买数量绑定存储
class Cart
{
private:
	// --------------------------------------------------------
    // 成员变量：items（购物清单）
    // 类型：vector<pair<Food, int>>
    // 说明：
    //   - pair 的第一个元素：Food 对象，存储食品的完整信息
    //   - pair 的第二个元素：int，存储该食品的购买数量
    // 示例：{ {可乐, 2}, {薯条, 1} } 表示2杯可乐 + 1份薯条
    // --------------------------------------------------------
    std::vector<std::pair<Food, int>> items;  // 存储已购食品及数量

public:
	// ============================================================
    // 方法：addItem（添加商品到购物车）
    // 参数： 
    //   - quantity：int 购买数量
    // 功能：将指定食品及其数量添加到购物清单中
    // 说明：如果购物车中已存在该食品，一般会累加数量而非重复添加
    // ============================================================
    void addItem(const Food& food, int quantity);
    
    // ============================================================
    // 方法：calculateTotal（计算购物车总金额）
    // 返回值：double 所有食品的总价（单价×数量 的累加和）
    // 说明：
    //   - 计算逻辑：遍历 items，累加 food.price * quantity
    // ============================================================
    double calculateTotal() const;
    
    // ============================================================
    // 方法：printReceipt（打印购物小票）
    // 参数：
    //   - cashPaid：double 顾客实际支付的现金金额
    // 功能：
    //   - 列出所有购买商品的明细（名称、数量、单价、小计）
    //   - 显示总金额
    //   - 计算并显示找零金额（cashPaid - 总金额）
    // 说明：const 成员函数，只展示数据不做修改
    // ============================================================
    void printReceipt(double cashPaid) const;
    
    // ============================================================
    // 方法：clear（清空购物车）
    // 功能：移除购物车中的所有商品
    // 说明：
    //   - 调用 vector 的 clear() 方法清空 items
    //   - 通常在顾客完成支付后调用，为下一位顾客做准备
    // ============================================================
    void clear();
    
    // ============================================================
    // 方法：isEmpty（判断购物车是否为空）
    // 返回值：bool
    //   - true：购物车中没有商品
    //   - false：购物车中有商品
    // 功能：用于判断顾客是否选购了商品
    // 说明：
    //   - const 成员函数，只查询不修改
    //   - 可判断是否允许结算或退出
    // ============================================================
    bool isEmpty() const;
};

#endif
